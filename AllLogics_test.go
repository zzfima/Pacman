package main

import (
	"testing"

	"github.com/stretchr/testify/require"
)

func TestLoadMaze(t *testing.T) {
	mazeExpected := []string{
		"############################",
		"#............##............#",
		"#.####.#####.##.#####.####.#",
		"#X####.#####.##.#####.####X#",
		"#..........................#",
		"#.####.##.########.##.####.#",
		"#......##....##....##......#",
		"######.##### ## #####.######",
		"     #.##          ##.#     ",
		"     #.## ###--### ##.#     ",
		"######.## # GGGG # ##.######",
		"      .   # GGGG #   .      ",
		"######.## # GGGG # ##.######",
		"     #.## ######## ##.#     ",
		"     #.##    P     ##.#     ",
		"######.## ######## ##.######",
		"#............##............#",
		"#.####.#####.##.#####.####.#",
		"#X..##................##..X#",
		"###.##.##.########.##.##.###",
		"#......##....##....##......#",
		"#.##########.##.##########.#",
		"#..........................#",
		"############################",
	}

	mazeActual, _ := LoadMaze("maze01.txt")
	require.EqualValues(t, mazeActual, mazeExpected)
}
